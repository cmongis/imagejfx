package ijfx.core.hash;

import java.io.File;
import java.io.IOException;
import org.scijava.service.Service;

/**
 *
 * @author Cyril Quinton
 */
public interface HashService extends Service {

    public static final char[] hexArray = "0123456789ABCDEF".toCharArray();
    /**
     * Gives a unique String that identify a file.
     * @param file the file used to calculate the unique id. 
     * @return
     * @throws IOException 
     */
    public String getHash(File file) throws IOException;

    
    
    public static String bytesToHex(byte[] bytes) {
        char[] hexChars = new char[bytes.length * 2];
        for (int j = 0; j < bytes.length; j++) {
            int v = bytes[j] & 0xFF;
            hexChars[j * 2] = hexArray[v >>> 4];
            hexChars[j * 2 + 1] = hexArray[v & 0x0F];
        }
        return new String(hexChars);
    }
}
